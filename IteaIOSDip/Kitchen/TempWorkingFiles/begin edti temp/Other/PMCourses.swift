 Описание курса
 Курс Agile/Scrum – это обучение гибкому мировоззрению, гибким подходам к разработке продукта и организации гибких проектов. Программа подходит как для тех, кто уже знаком на практике с данными методами и имеет опыт работы в гибких проектах, но столкнулся со сложностями их применения, так и для новичков, которые только планируют или начинают работать с Agile/Scrum. Освоение agile концепции и гибких практик позволит вам организовать проект более эффективно. Вы получите ответы на все вопросы, актуальные для начинающих проектных менеджеров, которые желают строить работу на базе Agile/Scrum. Курс ведут преподаватели из IT-компаний. Каждый урок даст возможность изучить новые тонкости использования гибкой методологии на практике. Вы узнаете, почему extreme programming повышает качество программного продукта и как использовать Agile/Scrum-подходы в конкретных целях и ситуациях.
