 Project Management Professional / PMP
 
 
 Описание курса
 Курс Project management Professional от ITEA и PMI Kiev Chapter— это самая полная и системная подготовка для опытных руководителей проектов на основе стандарта PMBOK. Данный курс необходим всем, кто занимается управлением проектами и желает повысить свою квалификацию до профессионального уровня. Это курс для опытных руководителей проектов и всех, чья работа связана с повышением эффективности управления проектами организации - менеджерам портфелей проектов, руководителям проектных офисов и т.д. Программа данного курса полностью соответствует требованиям PMI для подготовки и допуска к экзамену на получение сертификата PMP® (Project management Professional®) от PMI. За прохождение курса вам будут начислены контактные часы, которые необходимы для доступа к PMP® экзамену. Детальнее о сертификации и экзамене можно ознакомиться по ссылке https://www.pmi.org/certifications/types/project-management-pmp).
 После курса вы сможете:
 Понимать связи между областями знаний, группами процессов PMBOK®, входами, выходами процессов
 Применять методы и инструменты PMBOK® в своих проектах
 Улучшить свои навыки управления проектами за счет адаптации документации, методов и инструментов PMBOK® к специфике организации, клиента, проекта
 Получить необходимые часы обучения для подачи заявки на сертификацию PMP® (PMI)
 Разработать план подготовки к экзамену на сертификацию PMP® (PMI)
